function makeUpperCase(str){
    /**
     * P: the function takes in a string in any case, always
     * R: returns the string recieved converted into uppercase
     * E: "mangoe" => "MANGOE" or "This is a sample COde" => "THIS IS A SAMPLE CODE"
     * P:
     * - convert the string recieved to uppercase
     * - return string
     */
    return str.toUpperCase()
}
